public class IntegerDivision {
    public static void main(String[] args) {
        System.out.println();

        double quotient = (float)5 / 2;
        System.out.println("The quotient is " + quotient);

        int number1 = 5;
        int number2 = 2;
        quotient = number1 / number2;
        System.out.println("The quotient is " + quotient);

        System.out.println();
    }
}
