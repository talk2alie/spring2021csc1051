/**
 * Smile
 */
public class Smile {
    public static void main(String[] args) {
        System.out.println();

        System.out.println("****\t\t\t\t****");
        System.out.println(" *   *\t\t\t      *   *");
        System.out.println("  *   *\t\t\t     *   *");
        System.out.println("   *   *\t\t    *   *");
        System.out.println("    *   *\t\t   *   *");
        System.out.println("     *   *\t\t  *   *");
        System.out.println("      *   *\t\t *   *");
        System.out.println("       *   *\t\t*   *");
        System.out.println("        *   *\t       *   *");
        System.out.println("         *   *\t      *   *");
        System.out.println("          *   *\t     *   *");
        System.out.println("           *   *    *   *");
        System.out.println("            *   *  *   *");
        System.out.println("             *   **   *");
        System.out.println("\t      *      *");
        System.out.println("\t       *    *");
        System.out.println("\t\t****");

        System.out.println();
    }
}