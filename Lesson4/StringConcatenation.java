public class StringConcatenation {
    public static void main(String[] args) {
        System.out.println();

        String studentName = "Koo, Grace D.";
        int numberOfStudents = 28;
        int averageStudentAge = 18;
        System.out.println("There are " + numberOfStudents + " students in the class. " + 
        studentName + " is one of those students. The average age of the students is " + averageStudentAge);


        int birthYear = 1991;
        int laterYear = birthYear;
        System.out.println("birthYear" + laterYear);

        System.out.println();
    }
}
