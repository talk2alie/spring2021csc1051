public class RelationalOperators {
    public static void main(String[] args) {
        System.out.println();

        int leftOperand = 5;
        int rightOperand = 5;
        if(leftOperand != rightOperand) 
            System.out.println(leftOperand + " is not equal to " + rightOperand);
        else
            System.out.println(leftOperand + " is equal to " + rightOperand);
       

        System.out.println();
    }
}
