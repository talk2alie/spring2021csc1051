/**
 * SquareArea
 */
public class SquareArea {

    public static void main(String[] args) {
        System.out.println();

        double length, width, area;

        length = 10;

        width = 5;

        area = length * width;
        
        System.out.println("The area of a shape with dimension; " + length + "x" + width + " is " + area);

        System.out.println();
    }
}

