public class BooleanDataType {
    public static void main(String[] args) {
        System.out.println();

        boolean isValid;
        isValid = true;
        System.out.println(isValid);

        boolean isGreenLightOn;
        isGreenLightOn = false;
        System.out.println(isGreenLightOn);

        System.out.println();
    }
}
