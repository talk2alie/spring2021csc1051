public class UsingCharacters {
    public static void main(String[] args) {
        System.out.println();

        char firstLetter;
        firstLetter = 'A';

        char secondLetter;
        secondLetter = 'l';

        char thirdLetter;
        thirdLetter = 'i';

        char fourthLetter;
        fourthLetter = 's';

        char fifthLetter = 'a';
        System.out.println("Her name is " + firstLetter + secondLetter + thirdLetter + fourthLetter + fifthLetter);

        System.out.println();
    }
}
